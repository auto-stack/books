def main():
    # Variable declarations
    i = 5
    print(i)

    i = i + 1
    print(i)

    # Variables can hold different types
    name = "Alice"
    score = 98.5
    is_passing = True

    # Multi-line string in a variable
    s = """This is a multi-line string.
This is the second line."""

    print(name)
    print(score)
    print(is_passing)
    print(s)


if __name__ == "__main__":
    main()
