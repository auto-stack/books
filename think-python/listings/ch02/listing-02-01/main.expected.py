def main():
    # Creating variables
    n = 17
    pi = 3.141592653589793
    message = "And now for something completely different"

    # Using variables in expressions
    print(n + 25)
    print(2 * pi)

    # Using variables in function calls
    print(round(pi))
    print(len(message))

    # Mutable variable
    x = 5
    print(x)
    x = 10
    print(x)


if __name__ == "__main__":
    main()
